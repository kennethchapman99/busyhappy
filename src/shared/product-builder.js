function ensureDir(dir) { fs.mkdirSync(dir, { recursive: true }); }
function safe(text) { return String(text ?? '').replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;'); }

function buildPagePlan(sku, family) {
  const base = [
    { type: 'cover', title: sku.title, instructions: 'Use as the main printable cover page.' },
    { type: 'instructions', title: 'How to use this pack', instructions: `Print the pages that match your ${family.useCase} moment. Bring pencils or crayons.` },
    { type: 'find-and-circle', title: `${family.useCase} find and circle`, instructions: `Circle the ${sku.theme} items you can spot.` },
    { type: 'draw-and-tell', title: 'Draw and tell', instructions: `Draw your favorite ${family.useCase} moment and tell someone about it.` },
    { type: 'count-and-color', title: 'Count and color', instructions: `Count the themed items and color the matching number.` },
    { type: 'mini-game', title: `${family.useCase} mini game`, instructions: `Play a simple quiet-time game for this setting.` },
  ];
  if (sku.formatType.includes('journal')) {
    base.push({ type: 'memory-page', title: 'Trip memory page', instructions: 'Write or draw what you saw, ate, or loved most.' });
  }
  if (family.useCase.includes('road trip') || family.useCase.includes('scavenger')) {
    base.push({ type: 'scavenger', title: 'Scavenger hunt', instructions: 'Check off what you find on the road.' });
  }
  if (family.useCase.includes('airport') || family.useCase.includes('airplane')) {
    base.push({ type: 'bingo', title: 'Travel bingo', instructions: 'Mark each thing when you spot it.' });
  }
  return base;
}

function buildListingCopy(sku, family, pagePlan) {
  return `# ${sku.title}\n\n## Positioning\nA screen-free printable ${family.useCase} activity pack for ages ${sku.ageBand}.\n\n## Promise\nHelp parents buy 20-60 minutes of calm during ${family.useCase} time.\n\n## Included\n${pagePlan.map((page, i) => `- Page ${i + 1}: ${page.title}`).join('\n')}\n\n## Etsy title draft\n${sku.title} | ${family.useCase} printable for kids | ${sku.theme} theme\n\n## Etsy bullets\n- Instant printable activity pack\n- Designed for ages ${sku.ageBand}\n- Great for ${family.useCase} time\n- Screen-free quiet activity\n- Theme: ${sku.theme}\n\n## Notes\nThis is a real build artifact generated by the repo. It is not a live marketplace listing.`;
}

