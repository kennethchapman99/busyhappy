export function buildSkuProductPackage(sku, family) {
  const dir = join(OUTPUT_ROOT, sku.id);
  ensureDir(dir);
  const pagePlan = buildPagePlan(sku, family);
  const html = buildHtml(sku, family, pagePlan);
  const listingCopy = buildListingCopy(sku, family, pagePlan);
  const manifest = {
    skuId: sku.id,
    title: sku.title,
    familyId: family.id,
    familyTitle: family.title,
    outputDir: dir,
    files: ['activity-pack.html', 'activity-plan.json', 'listing-copy.md', 'manifest.json'],
    builtAt: new Date().toISOString(),
    status: 'draft_artifacts_generated'
  };
  fs.writeFileSync(join(dir, 'activity-pack.html'), html, 'utf8');
  fs.writeFileSync(join(dir, 'activity-plan.json'), JSON.stringify(pagePlan, null, 2), 'utf8');
  fs.writeFileSync(join(dir, 'listing-copy.md'), listingCopy, 'utf8');
  fs.writeFileSync(join(dir, 'manifest.json'), JSON.stringify(manifest, null, 2), 'utf8');
  return { dir, manifest };
}

export function buildAllProductPackages(skus, families) {
  ensureDir(OUTPUT_ROOT);
  const familyMap = Object.fromEntries(families.map((family) => [family.id, family]));
  const built = [];
  for (const sku of skus) {
    built.push(buildSkuProductPackage(sku, familyMap[sku.productFamilyId]));
  }
  fs.writeFileSync(join(OUTPUT_ROOT, 'catalog-manifest.json'), JSON.stringify({ builtAt: new Date().toISOString(), count: built.length, items: built.map((b) => b.manifest) }, null, 2), 'utf8');
  return built;
}

export function getBuildStatusForSku(skuId) {
  const dir = join(OUTPUT_ROOT, skuId);
  const manifestPath = join(dir, 'manifest.json');
  if (!fs.existsSync(manifestPath)) return null;
  try { return JSON.parse(fs.readFileSync(manifestPath, 'utf8')); } catch { return null; }
}
